// Define a Java class named "Simple."
class Simple {
    // Define the main method, which is the entry point of the Java program.
    public static void main(String args[]) {
        // Print the string "Hello Java" to the standard output (console).
        System.out.println("Hello Java");
    }
}


// Here's an explanation of each part of the code:

// class Simple { ... }: This line defines a Java class named "Simple." In Java, all code must be inside a class, and the name of the class should match the filename (if not part of a larger project with specific naming conventions).

// public static void main(String args[]) { ... }: This line defines the main method, which is a special method in Java. It serves as the entry point for the Java program. It has the following components:

// public: This access modifier allows the main method to be accessed from outside the class. It's required for the program to start.
// static: This keyword indicates that the main method belongs to the class itself and not to any specific instance of the class.
// void: This indicates that the main method does not return any value.
// main: This is the name of the method.
// (String args[]): This is the parameter list for the main method. It accepts an array of strings called args. These arguments can be passed to the program when it's executed, allowing you to provide input or options to the program.
// System.out.println("Hello Java");: This line is inside the main method and uses the System.out.println statement to print the string "Hello Java" to the standard output (usually the console). It's a simple example of how to display text in the console when the program is run.